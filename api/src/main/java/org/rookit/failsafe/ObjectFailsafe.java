package org.rookit.failsafe;

import org.slf4j.Logger;

public interface ObjectFailsafe {
    <T> T is(Logger logger, boolean condition, String message, Object... args);

    <T> T isEquals(Logger logger, Object expected, Object actual, String name);

    <T> T isNotEquals(Logger logger, Object object, Object unexpected, String objectName);

    <T> T isNotNull(Logger logger, Object object, String name);

    <T> T isNull(Logger logger, Object object, String name);
}
